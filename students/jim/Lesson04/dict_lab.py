# Dictionaries 1

my_dict = {'name':'Chris', 'city':'Seattle', 'cake':'Chocolate'}
print(my_dict)

my_dict.pop('cake')

print('cake' in my_dict)
print(my_dict)

my_dict['fruit'] = 'Mango'
print(my_dict)
print(my_dict.keys())
print(my_dict.values())
print('cake' in my_dict)
print('Mango' in my_dict.values())
